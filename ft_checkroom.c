# include "lem-in.h"

int		ft_checkroom(char *s)
{
	int space;

	space = 0;
	if(*s == 'L' || *s == '#')
		return (0);
	while(*s)
	{
		if (*s == '-')
			return (0);
		if(*s == ' ')
			space++;
		if(space > 2)
			return (0);
		s++;
	}
	return (1);
}
