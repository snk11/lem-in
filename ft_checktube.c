# include "lem-in.h"

int		ft_checktube(char *s)
{
	int	t;

	t = 0;
	while(*s)
	{
		if (*s == ' ')
			return (0);
		if(*s == '-')
			t++;
		if (t > 1)
			return (0);
		s++;
	}
	if(t == 1)
		return (1);
	return (0);
}
